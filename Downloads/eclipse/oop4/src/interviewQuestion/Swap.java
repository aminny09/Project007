package interviewQuestion;

public class Swap {
	 
	 public static void main(String[] args) {
	     
	   int a, b,st;
	   a = 15;
	   b = 27;
	   System.out.println("Before swapping : a, b = "+a+", "+ + b);
	   st=a;
	   a = b;
	   b =st;   
	  System.out.println("After swapping : a, b = "+a+", "+ + b);
	  
	 }
}
