public class Greatnum {

	public static void main(String[] args) {
   boolean x=greatnum(7,1);
   System.out.println(x);
    boolean y=greatnum(34,27);
    System.out.println(y);
	}
	public static boolean greatnum(int a,int b) {
		  if(a==7||a==7) {
			  return true;
		  }
		  else if(a+b==7) {
			  return true;
		  }
			  else if(a-b==7) {
				  return true;
			  }  
				  else{
					  return false;
					  
		  }
	   }	  
	}
