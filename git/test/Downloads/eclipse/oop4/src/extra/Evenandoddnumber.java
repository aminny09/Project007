package extra;

public class Evenandoddnumber {

	public static void main(String[] args) {

	      print(18);
	      print(15);
	}
	
	public static void print(int x) {     
		if(x%2==0) {
			System.out.println("Even number:"+x);
		}  
      else{
    	  System.out.println("Odd number:"+x);
    	     	  
      }
   }
}


